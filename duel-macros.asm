
; ==============================================================
; Additional macros by LB
; ==============================================================


; ==============================================================
; Macro to position the cursor
; ==============================================================

  MAC PLOT
  ldy #{1}
  ldx #{2}
  clc
  jsr $E50A  ; PLOT 
  ENDM

; ==============================================================
; Macro to print a string
; ==============================================================

  MAC PRINTSTRING
  ldy #>{0}
  lda #<{0}
  jsr $ab1e ; STROUT 	
  ENDM

; ==============================================================
; Macro to print a byte
; ==============================================================

	MAC PRINTBYTE
  ldx #$00
  ldy #$0a
  lda {0}
  jsr printnum
  ENDM

; ==============================================================
; Macro to print a word
; ==============================================================

  MAC PRINTWORD
  lda #<{0}
  ldx #>{0}
  ldy #$0a
  jsr printnum
  ENDM

; ==============================================================
; Macro to print an IP address
; ==============================================================
  MAC PRINT_IP
  ldx #>({0})
  lda #<({0})
  jsr printip
  PRINT CRLF
  ENDM
